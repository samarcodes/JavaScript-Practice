var num1 = 10
var num2 = 15
var num3 = 20
//Addition
console.log(num1 + num2)
console.log(num1 + num2 + num3)

//Subraction
console.log(num2 - num1)
console.log(num3 - num2 - num1)

//Multiplication
console.log(num1 * num2)

//Division
console.log(num2 / num1)

//Modulus
console.log(num2 % num1)

//Increment - Post and Pre
console.log(num1++) //prints 10 and increment num1 to 11
console.log(num1) //prints 11

console.log(++num1) //increment num1 to 11 and prints it

//Decrement - Post and Pre
console.log(num1--) //prints 10 and decrement num1 to 9
console.log(num1) //prints 9

console.log(--num1) //decrement num1 to 9 and prints it